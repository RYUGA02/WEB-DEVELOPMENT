// if Condition statement
let speed = 60;
if (speed >= 60) {
    console.log("Sir! you will get Charged for overspeeding.");
}


// If-else Statements
if (speed > 60) {
    console.log("You are fined!");
}
else {
    console.log("you are not fined!");
}


